def _(key: str) -> str:
    """
    Main i18n wrapper replacing inline strings.
    Reads global `app_language` ('en' or 'ro') from Lingua Config on call.
    Uses 'en' as natural fallback if a key is unregistered.
    """
    config = get_config()
    lang = config.get("app_language", "en")  # Default to english
    
    layer = TRANSLATIONS.get(lang, TRANSLATIONS["en"])
    return layer.get(key, key)
